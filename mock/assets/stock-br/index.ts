function ohlcAround(price: number, spread = 0.02) {
  return {
    open: Math.round(price * (1 - spread * 0.3) * 100) / 100,
    prevClose: Math.round(price * (1 - spread * 0.5) * 100) / 100,
    dayLow: Math.round(price * (1 - spread) * 100) / 100,
    dayHigh: Math.round(price * (1 + spread * 0.8) * 100) / 100,
  };
}

